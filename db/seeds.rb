    100.times do |i|
      tue_buy = tue1b
      tue_sell = tue1s
      tue_name = "#{name}[#{i}]"
      tue_buy += (tue1b/20) * i
      tue_sell += (tue1s/20) * i
      Item.create(buy: tue_buy, sell: tue_sell, kind: "tue", name: tue_name)
    end
  end
end

